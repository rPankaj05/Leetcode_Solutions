class Solution{
public:
    string removeSpecialCharacter(string s) {
       string ans;
       for(auto it:s){
           if((it>='a' and it<='z') or it>='A' and it<='Z') ans+=it;
       }
       
       if(ans=="") return "-1";
       return ans;
    }
};

