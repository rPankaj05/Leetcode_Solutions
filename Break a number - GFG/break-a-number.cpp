class Solution{   
public:
    int waysToBreakNumber(int N){
       
       long long int n=N;
       long long int mod=1e9+7;
       long long int ans=0;
       
       ans=((n+1)*(n+2)%mod/2)%mod;
       
       return ans;
         
    }
};

